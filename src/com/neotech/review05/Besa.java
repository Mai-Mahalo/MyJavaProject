package com.neotech.review05;

// Part-1a

public class Besa {
	
	
	// No parameters and No return type
	void addTwoNumbers() {
		int result = 8 + 7;
	}
	
	// TWO parameters and NO return type
	void addTwoThatIGive(int a, int b) {
		int result = a + b;
		
	}
	
	// NO parameters and WITH return type
	int addTwoNumbersAndGiveMeTheResult() {
		int result = 8 + 7;
		return result;
		
	}
	// TWO paraemeters and WITH return type
	int addTwoNumbersAndGiveMeTheResult(int num1, int num2) {
		int result = num1 + num2;
		return result; 
	}

}
